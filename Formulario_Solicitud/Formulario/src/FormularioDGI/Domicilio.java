package FormularioDGI;

public class Domicilio {
    private String calle;
    private String numero;
    private String ciudad;
    private String departamento;
    private String numCasa;
    private String distrito;
    private String nomSector;
    private String telefono;
    private String correo;
    private String codigoPostal;
    private String celular;
    private String municipio;
    


    public Domicilio() {}

    public String getCalle() { return calle; }
    public void setCalle(String calle) { this.calle = calle; }

    public String getNumero() { return numero; }
    public void setNumero(String numero) { this.numero = numero; }

    public String getCiudad() { return ciudad; }
    public void setCiudad(String ciudad) { this.ciudad = ciudad; }

    public String getDepartamento() { return departamento; }
    public void setDepartamento(String departamento) { this.departamento = departamento; }

    public String getNumCasa() { return numCasa; }
    public void setNumCasa(String numCasa) { this.numCasa = numCasa; }

    public String getDistrito() { return distrito; }
    public void setDistrito(String distrito) { this.distrito = distrito; }

    public String getNomSector() { return nomSector; }
    public void setNomSector(String nomSector) { this.nomSector = nomSector; }

    public String getTelefono() { return telefono; }
    public void setTelefono(String telefono) { this.telefono = telefono; }

    public String getCorreo() { return correo; }
    public void setCorreo(String correo) { this.correo = correo; }

    public String getCodigoPostal() { return codigoPostal; }
    public void setCodigoPostal(String codigoPostal) { this.codigoPostal = codigoPostal; }

    public String getCelular() { return celular; }
    public void setCelular(String celular) { this.celular = celular; }

    public String getMunicipio() { return municipio; }
    public void setMunicipio(String municipio) { this.municipio = municipio; }
}
