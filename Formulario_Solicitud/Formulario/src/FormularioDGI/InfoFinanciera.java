package FormularioDGI;

public class InfoFinanciera {
    private double ingresosAnuales;
    private double gastosAnuales;
    private String fuenteIngresos;

    public InfoFinanciera() {}

    public double getIngresosAnuales() { return ingresosAnuales; }
    public void setIngresosAnuales(double ingresosAnuales) { this.ingresosAnuales = ingresosAnuales; }

    public double getGastosAnuales() { return gastosAnuales; }
    public void setGastosAnuales(double gastosAnuales) { this.gastosAnuales = gastosAnuales; }

    public String getFuenteIngresos() { return fuenteIngresos; }
    public void setFuenteIngresos(String fuenteIngresos) { this.fuenteIngresos = fuenteIngresos; }
    
}
