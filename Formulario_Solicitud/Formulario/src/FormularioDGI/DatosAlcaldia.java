package FormularioDGI;

public class DatosAlcaldia {
    private String barrioDomicilio;
    private String barrioNegocio;
    private String nomLicenciaComercial;
    private String categoria;
    private String direcNegocio;
    private String actividadComercial;
    private String pyme;

    public DatosAlcaldia() {
        
    }

    public String getBarrioDomicilio() { return barrioDomicilio; }
    public void setBarrioDomicilio(String barrioDomicilio) { this.barrioDomicilio = barrioDomicilio; }

    public String getBarrioNegocio() { return barrioNegocio; }
    public void setBarrioNegocio(String barrioNegocio) { this.barrioNegocio = barrioNegocio; }

    public String getNomLicenciaComercial() { return nomLicenciaComercial; }
    public void setNomLicenciaComercial(String nomLicenciaComercial) { this.nomLicenciaComercial = nomLicenciaComercial; }

    public String getCategoria() { return categoria; }
    public void setCategoria(String categoria) { this.categoria = categoria; }

    public String getDirecNegocio() { return direcNegocio; }
    public void setDirecNegocio(String direcNegocio) { this.direcNegocio = direcNegocio; }

    public String getActividadComercial() { return actividadComercial; }
    public void setActividadComercial(String actividadComercial) { this.actividadComercial = actividadComercial; }

    public String getPyme() { return pyme; }
    public void setPyme(String pyme) { this.pyme = pyme; }
}