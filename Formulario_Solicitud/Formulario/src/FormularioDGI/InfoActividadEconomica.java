package FormularioDGI;

public class InfoActividadEconomica {
    private String actividadEcoPrincipal;
    private String fechaInicio;
    private String codigoCIIU;
    private String prodLider;
    private String actividadEcoSecundaria;
    private String fechaInicioSecundaria;
    private String codigoCIIUSecundaria;
    private String prodLiderSecundaria;

    public InfoActividadEconomica() {}

    public String getActividadEcoPrincipal() { return actividadEcoPrincipal; }
    public void setActividadEcoPrincipal(String actividadEcoPrincipal) { this.actividadEcoPrincipal = actividadEcoPrincipal; }

    public String getFechaInicio() { return fechaInicio; }
    public void setFechaInicio(String fechaInicio) { this.fechaInicio = fechaInicio; }

    public String getCodigoCIIU() { return codigoCIIU; }
    public void setCodigoCIIU(String codigoCIIU) { this.codigoCIIU = codigoCIIU; }

    public String getProdLider() { return prodLider; }
    public void setProdLider(String prodLider) { this.prodLider = prodLider; }

    public String getActividadEcoSecundaria() { return actividadEcoSecundaria; }
    public void setActividadEcoSecundaria(String actividadEcoSecundaria) { this.actividadEcoSecundaria = actividadEcoSecundaria; }

    public String getFechaInicioSecundaria() { return fechaInicioSecundaria; }
    public void setFechaInicioSecundaria(String fechaInicioSecundaria) { this.fechaInicioSecundaria = fechaInicioSecundaria; }

    public String getCodigoCIIUSecundaria() { return codigoCIIUSecundaria; }
    public void setCodigoCIIUSecundaria(String codigoCIIUSecundaria) { this.codigoCIIUSecundaria = codigoCIIUSecundaria; }

    public String getProdLiderSecundaria() { return prodLiderSecundaria; }
    public void setProdLiderSecundaria(String prodLiderSecundaria) { this.prodLiderSecundaria = prodLiderSecundaria; }
}